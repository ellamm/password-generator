const generateButton = document.getElementById("generate-btn");

generateButton.addEventListener("click", function () {
  const pass1 = document.getElementById("pass-1-button");
  const pass2 = document.getElementById("pass-2-button");
  pass1.textContent = generateRandomPassword(15);
  pass2.textContent = generateRandomPassword(12);
});

function getRandomCharacter() {
  const randomIndex = Math.floor(
    Math.random(characters.length) * characters.length
  );
  return characters[randomIndex];
}

function generateRandomPassword(passLength) {
  let password = "";
  for (let i = 1; i < passLength; i++) {
    password += getRandomCharacter();
  }
  return password;
}
